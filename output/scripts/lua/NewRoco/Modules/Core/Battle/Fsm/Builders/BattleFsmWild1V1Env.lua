local BattleFsmEnv = require("NewRoco.Modules.Core.Battle.Fsm.BattleFsmEnv")
local BattleEvent = require("NewRoco.Modules.Core.Battle.Common.BattleEvent")
local BattleFsmWild1V1Env = BattleFsmEnv:Extend("BattleFsmEnv")

function BattleFsmWild1V1Env:InitStates()
  self:BuildState("InitState")
  self:BuildState("EnterPerformState")
  self:BuildState("NearbyEnterState")
  self:BuildState("NearbyReconnectEnterState")
  self:BuildState("StandbyState")
  self:BuildState("SwapPlayState")
  self:BuildState("RoundPlayState")
  self:BuildState("RoundSelectState")
  self:BuildState("WaitingOtherState")
  self:BuildState("NormalOverState")
  self:BuildState("LeaveBattlePureBlackOutState")
  self:BuildState("SeamlessOverState")
  self:BuildState("DestroyState")
  self:BuildState("PrePlayState")
  self:BuildState("FailOverState")
  self:BuildState("DirectOverState")
  self:TransitionState("InitState")
  self:TransitionState("EnterPerformState")
  self:TransitionState("NearbyEnterState")
  self:TransitionState("NearbyReconnectEnterState")
  self:TransitionState("PrePlayState")
  self:TransitionState("StandbyState")
  self:TransitionState("SwapPlayState")
  self:TransitionState("RoundPlayState")
  self:TransitionState("RoundSelectState")
  self:TransitionState("WaitingOtherState")
  self:TransitionState("NormalOverState")
  self:TransitionState("LeaveBattlePureBlackOutState")
  self:TransitionState("SeamlessOverState")
  self:TransitionState("DestroyState")
  self:TransitionState("FailOverState")
  self:TransitionState("DirectOverState")
  self.BattleFsm:AddTransitionToState(BattleEvent.EnterNormalOver, self.NormalOverState)
  self.BattleFsm:AddTransitionToState(BattleEvent.EnterSeamlessOver, self.SeamlessOverState)
  self.BattleFsm:AddTransitionToState(BattleEvent.DirectOverBattle, self.DirectOverState)
end

return BattleFsmWild1V1Env
