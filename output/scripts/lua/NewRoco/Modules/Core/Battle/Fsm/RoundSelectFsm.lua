local BattleEnum = require("NewRoco.Modules.Core.Battle.Common.BattleEnum")
local BattleEvent = require("NewRoco.Modules.Core.Battle.Common.BattleEvent")
local Fsm = require("NewRoco.Modules.Core.Fsm.Fsm")
local RoundSkillAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundSkillAction")
local RoundSwapAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundSwapAction")
local RoundCatchAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundCatchAction")
local RoundItemAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundItemAction")
local RoundEscapeAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundEscapeAction")
local RoundNoneAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundNoneAction")
local RoundSurrenderAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundSurrenderAction")
local RoundStepAwayAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundStepAwayAction")
local RoundGiveUpAction = require("NewRoco.Modules.Core.Battle.Fsm.Actions.Round.RoundGiveUpAction")

local function CreateFsm()
  local fsm = Fsm("RoundSelectFsm")
  local CurrentPet = fsm:CreateVar("CurrentPet")
  local StateEvent = fsm:CreateVar("StateEvent")
  local SelectMarkerManager = fsm:CreateVar("MarkerManager")
  local CurrentSelectGuid = fsm:CreateVar("CurrentSelectGuid", 0)
  local SkillState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.SkillState)
  local SwapState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.SwapState)
  local CatchState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.CatchState)
  local ItemState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.ItemState)
  local EscapeState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.EscapeState)
  local SurrenderState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.SurrenderState)
  local StepAwayState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.StepAwayState)
  local GiveUpState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.GiveUpState)
  local NoneState = fsm:CreateSequentialState(BattleEnum.RoundStateNames.NoneState)
  SkillState:AddAction(RoundSkillAction("Skill"))
  SwapState:AddAction(RoundSwapAction("Swap", {CurrentSelectGuid = CurrentSelectGuid}))
  CatchState:AddAction(RoundCatchAction("Catch"))
  ItemState:AddAction(RoundItemAction("Item"))
  EscapeState:AddAction(RoundEscapeAction("Escape"))
  SurrenderState:AddAction(RoundSurrenderAction("Surrender"))
  StepAwayState:AddAction(RoundStepAwayAction("StepAway"))
  GiveUpState:AddAction(RoundGiveUpAction("GiveUp"))
  NoneState:AddAction(RoundNoneAction("None"))
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterSkill, SkillState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterSwap, SwapState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterCatch, CatchState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterItem, ItemState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterEscape, EscapeState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterSurrender, SurrenderState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterStepAway, StepAwayState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterGiveUp, GiveUpState)
  fsm:AddTransitionToState(BattleEvent.RoundEvent.EnterNone, NoneState)
  fsm:SetInitState(SkillState)
  return fsm
end

return CreateFsm
